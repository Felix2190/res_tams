	$xajax=new xajax();

	 function guardar($nombre,$estatus)
	{
		global $_NOW_;
		global $objSession;
		$r=new xajaxResponse();
		
		$Ubicacion = new ModeloUbicacion();
		$Ubicacion->setNombre($nombre);
		$Ubicacion->setEstatus($estatus);
		$Ubicacion->guardar();
		
		if($Ubicacion->getError())
		{
			
			$r->ocultarMensaje();
			$r->mostrarError($Ubicacion->getStrError());
			return $r;
		}
		
		$r->ocultarMensaje();
		$r->mostrarAviso("El registro se almaceno correctamente.");
		$r->redirect("listadoProductos.php");
		return $r;
		

	}
	$xajax->registerFunction("guardar");

	$xajax->processRequest();


