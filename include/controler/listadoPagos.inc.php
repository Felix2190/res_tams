	function calcularTablaPagos()
	{
		global $objSession;
		global $dbLink;
		
		//var_dump($objSession->getIdUbicacion());
		
		
		$query="SELECT 
					D.idTurno, 
					D.turnoExterno, 
					D.personaNombre, CONCAT_WS(' ',personaprimerApellido,personaSegundoApellido) As personaApellidos, 
					P.total AS total,
					P.fechaPago AS fecha,
					P.folioRecaudacion AS folio,
					CONCAT_WS(' ',T.descripcion,T.tipo,T.periodo,'meses',T.tipoTramite) AS tramite
				FROM turnoDetalles As D
				INNER JOIN pago AS P ON P.idTurno=D.idTurno
				INNER JOIN tipolicencia AS T ON T.idTipoLicencia=D.idTipoLicencia
				WHERE P.estatus='pagado'";
		
		$result=mysqli_query($dbLink, $query);
		if(!$result)
		{
			die("Error en la consulta");
		}
		
		$tabla="";
		while($r=mysqli_fetch_assoc($result))
		{
			/*
			<th>Turno</th>
			<th>Externo</th>
			<th>Folio Recaudaci&oacute;n</th>
			<th>Nombre(s)</th>														
			<th>Tipo de Licencia</th>
			<th>Total</th>														
			<th>Opciones</th>
			*/
			
			$tabla.='<tr>
						<td>' . $r["idTurno"] . '</td>
						<td>' . $r["turnoExterno"] . '</td>
						<td>' . $r["folio"] . '</td>
						<td>' . $r["personaNombre"] . '</td>						
						<td>' . $r["tramite"] . '</td>
						<td>$ ' . number_format($r["total"],2 , "." , ",") . '</td>
						<td></td>
					</tr>';
			
		}
		
		return $tabla;
		
		/*
		<th>ID</th>
		<th>Externo</th>
		<th>Nombre(s)</th>
		<th>Apellidos</th>
		<th>Tipo de Licencia</th>
		<th>Opciones</th>
		*/
	}


