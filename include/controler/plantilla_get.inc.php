function armar_filtros()
{
	$filtros = '';
	if(isset($_GET['filter']) && $_GET['filter']!='')
	{
		foreach ($_GET['filter'] as $clave => $valor)
		{
			if($clave==0)
			{
				$filtros .= " AND  LIKE '".$valor."%' ";
			}else if($clave==1)
			{
				$filtros .= " AND  LIKE '".$valor."%' ";
			}
		}
	}
	return $filtros;
}


function ordenar()
{
	$ordenar = '';
	if(isset($_GET['col']) && $_GET['col']!='')
	{
		foreach ($_GET['col'] as $clave => $valor)
		{
			if($clave==0)
			{
				if($valor==0)
				{
					$ordenar = ' ORDER BY  ASC ';
				}
				else
				{
					$ordenar = ' ORDER BY  DESC ';
				}
			}
			else if($clave==1)
			{
				if($valor==0)
				{
					$ordenar = ' ORDER BY  ASC ';
				}
				else
				{
					$ordenar = ' ORDER BY  DESC ';
				}
			}
			
		}
	}
		return $ordenar;
}

