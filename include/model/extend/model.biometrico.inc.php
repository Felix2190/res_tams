		public function getKeyHuellas()
		{
			$arrInfo= array();
			try
			{
				$SQL="SELECT clave,nombre FROM biometrico
					WHERE tipo='huella'";
				mysqli_query($this->dbLink,"SET NAMES 'utf8'");
				$result=mysqli_query($this->dbLink,$SQL);
				if($result&&mysqli_num_rows($result)>0)
					while ($row=mysqli_fetch_assoc($result)){
					$arrInfo[]=array($row['clave'],$row['nombre']);
					}
					
				
				return $arrInfo;
			}
			catch (Exception $e)
			{
