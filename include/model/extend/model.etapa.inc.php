		public function getEtapaByOrden()
		{
			try
			{
				$SQL="SELECT
						idEtapa,descripcion,orden
					FROM etapa
					WHERE orden=" . mysqli_real_escape_string($this->dbLink,$this->orden);
					
				$result=mysqli_query($this->dbLink,$SQL);
				if(!$result)
					return $this->setSystemError("Error en la obtencion de detalles de registro.","[ModeloBaseEtapa::getDatos][" . $SQL . "][" . mysqli_error($this->dbLink) . "]");
		
		
					if(mysqli_num_rows($result)==0)
					{
						$this->limpiarPropiedades();
					}
					else
					{
						$datos=mysqli_fetch_assoc($result);
						foreach($datos as $k=>$v)
						{
							$campo="" . $k;
							$this->$campo=$v;
						}
					}
					return true;
			}
			catch (Exception $e)
			{
				return $this->setErrorCatch($e);
			}
		}
