		public function getByListado($listado="")
		{
			if($listado=="")
				$listado=$this->listado;
			if(is_null($listado)||$listado=="")
				return $this->setError("No se especifico el listado para la busqueda de los listado");
					
			$query = "SELECT
						idListado,valor
					FROM listadoscortos
					WHERE listado='" . mysqli_real_escape_string($this->dbLink,$listado) . "' ";
			
			$result = mysqli_query($this->dbLink,$query);
			if (! $result)
				return $this->setSystemError(
						"Error en una consulta a la base de datos, intentalo de nueva cuenta mas tarde.",
						"[model.ModeloBaseListadoscortos::LN36][" . $query . "][" . mysql_error() .
						"]");

			$retorno = array();
			while ($row = mysqli_fetch_assoc($result))
				$retorno[$row['idListado']] = $row['valor'];
			
			return $retorno;
		}

		
		
