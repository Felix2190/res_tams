        $arreglo = '';
				if(mysqli_num_rows($result)>0)
				{
					while ( $row_inf = mysqli_fetch_assoc ( $result ) ) {
            $menu = $row_inf['menu'];
            $arreglo[$menu] = $row_inf['permisos'];
