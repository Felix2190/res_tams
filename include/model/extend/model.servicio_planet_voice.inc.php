		public function getListaServiciosPlanetVoice()
		{
			$query = "SELECT S.idServicioPlanetVoice,P.nombre,S.fecha_solicitud,S.cantidad,S.renta_mensual,S.plazo FROM ".$this->_nombreTabla." AS S
					INNER JOIN producto as P on S.codigo=P.codigo ORDER By S.fecha_solicitud ASC";
			$result = mysqli_query($this->dbLink,$query);
			if (! $result)
				return $this->setSystemError(
						"Error en una consulta a la base de datos, intentalo de nueva cuenta mas tarde.",
						"[model.".$this->_nombreTabla."::LN36][" . $query . "][" . mysql_error() .
						"]");
				$retorno = array();
				while ($row = mysqli_fetch_assoc($result))
					$retorno[$row['idServicioPlanetVoice']] = array('nombre'=>$row['nombre'],
							'fecha_solicitud'=>$row['fecha_solicitud'],
							'cantidad'=>$row['cantidad'],
							'renta_mensual'=>$row['renta_mensual'],
							'plazo'=>$row['plazo'],
					);
					return $retorno;
		}
		
