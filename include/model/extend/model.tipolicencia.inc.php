		public function tramitesNuevos(){
			$retorno=array();
			try
			{
				
				
				$SQL="SELECT idTipoLicencia,CONCAT(descripcion,tipo,' ',periodo,' ','meses') AS tramite  FROM `tipolicencia` WHERE 1 AND tipoTramite='nueva' ";
				
				$result=mysqli_query($this->dbLink,$SQL);
